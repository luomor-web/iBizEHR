        const view: any = {
            viewname: 'att-endance-mreport-edit-view', 
            height: 0, 
            width: 0,  
            title: this.$t('entities.attendancemreport.views.editview.title'),
            placement: 'DRAWER_TOP',
        };
        openDrawer(view, data);
    }


